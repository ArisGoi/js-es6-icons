function randomColor(){
    let num = Math.floor(Math.random() * 3);
    if(num == 0){
        return "#EB4414"; //RED
    } else if(num == 1){
        return "#F0C105"; //YELLOW
    } else if(num == 2){
        return "#00D15E"; //GREEN
    }
}

